package datatypeDemo;

public class FloatDemo {
    public static void main(String[] args){
        // float and double
        float my_marks = (float)89.60;
        float my_math_marks = 90.68f;
        double my_total_marks = 29302.23232;
        System.out.println(my_marks);
        System.out.println(my_math_marks);
        System.out.println(my_total_marks);

    }
}
