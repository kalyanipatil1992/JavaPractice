package datatypeDemo;

public class VariableDemo {
    public static void main(String[] args){
        // byte,short,int and long
        System.out.println("-- byte,short,int and long --");
        byte marks = 98;
        System.out.println("My total marks are " +marks);

        short total_money = 150;
        System.out.println("Total money I have is " +total_money);

        int mobileNumber = 888080808;
        System.out.println("My Mobile number is " +mobileNumber);

        long mobileNumber1= 989989232;
        System.out.println("Mobile Number: " +mobileNumber1);
    }
}